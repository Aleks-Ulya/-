def is_palindrome(s: str) -> bool:
    """
    Рекурсивная функция проверки строки на палиндром.
    :param s: входная строка
    :return: True, если строка — палиндром; False иначе
    """
    # Нормализация: приводим к нижнему регистру и удаляем пробелы
    s = s.lower().replace(" ", "")
    
    # Базовый случай 1: пустая строка или один символ — палиндром
    if len(s) <= 1:
        return True
    
    # Базовый случай 2: первый и последний символы не совпадают — не палиндром
    if s[0] != s[-1]:
        return False
    
    # Рекурсивный вызов: проверяем подстроку без первого и последнего символов
    return is_palindrome(s[1:-1])



# Пример использования
if __name__ == "__main__":
    test_strings = [
        "шалаш",
        "А роза упала на лапу Азора",
        "привет",
        "abcba",
        "a"
    ]
    
    for text in test_strings:
        result = is_palindrome(text)
        print(f"'{text}' -> {result}")

